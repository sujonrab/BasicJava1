package basicjava;

public class OperatorDemo {
	
	/*
	 * 
	 * Arithmetic +, -, *, /, %
	 * Bitwise
	 * Relational
	 * Logical
	 */

	public static void main(String[] args) {
		
		
		int m = 4;
		int n = 5;
		
		++n; // pre increment
		n++; // post increment
		
		
		m= ++n;
		
		System.out.println(m);
		System.out.println(n);
	
		
		

	}

}
